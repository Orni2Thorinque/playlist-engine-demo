export const AFFECTED_COLORS: string[] = [];

export function getRandomColor(): string {
    let color = 'BLACK';

    do {
        const index = Math.floor(Math.random() * (colors.length + 1));
        color = colors[index];
    } while (AFFECTED_COLORS.indexOf(color) >= 0 && AFFECTED_COLORS.length < colors.length);

    AFFECTED_COLORS.push(color);
    return color;
}

